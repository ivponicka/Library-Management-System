    public boolean validationLogin(){
        String name = txt_username.getText();
        String password = txt_password.getText();
        
        if(name.equals("")){
            JOptionPane.showMessageDialog(this, "Enter a valid name");
            return false;
        }
        
         if(password.equals("")){
            JOptionPane.showMessageDialog(this, "Enter a valid password");
            return false;
        }
         return true;
    
    }
    
    public void login(){
        String name = txt_username.getText();
        String password = txt_password.getText();
        
        try {
            Connection con = DBConnection.getConnection();
            PreparedStatement statement = con.prepareStatement("select * from users where name=? and password=?");
            statement.setString(1, name);
            statement.setString(2, password);
            
            ResultSet results = statement.executeQuery();
            if(results.next()){
                JOptionPane.showMessageDialog(this, "Logged in successfully!");
                HomePage home = new HomePage();
                home.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Something went wrong. Try again. ");
            }
        } catch (HeadlessException | SQLException e) {
            e.getMessage();
            System.out.println(e.getMessage());
        }
    }
   
    
    
